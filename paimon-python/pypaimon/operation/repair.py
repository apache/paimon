"""
Repair operation for Paimon tables.

This module verifies the metadata consistency chain:
  snapshot -> manifest list -> manifest files -> data files

For filesystem catalogs (no external metastore), verification focuses on:
  - Verifying snapshot files are readable and valid
  - Verifying manifest list files referenced by snapshots exist and are readable
  - Verifying manifest files referenced by manifest lists exist and are readable
  - Verifying data files referenced by manifest entries exist
  - Detecting dangling snapshot references (LATEST pointing to missing snapshot)
"""

import logging
import re
from dataclasses import dataclass, field
from typing import List, Optional

from pypaimon.common.file_io import FileIO
from pypaimon.common.json_util import JSON
from pypaimon.snapshot.snapshot import Snapshot

logger = logging.getLogger(__name__)


@dataclass
class RepairIssue:
    """Represents a single issue found during repair verification."""
    level: str  # "error" or "warning"
    category: str  # e.g. "snapshot", "manifest_list", "manifest", "data_file"
    message: str
    path: Optional[str] = None


@dataclass
class RepairReport:
    """Report generated by a repair operation."""
    table_path: str
    issues: List[RepairIssue] = field(default_factory=list)
    snapshots_checked: int = 0
    manifest_lists_checked: int = 0
    manifest_files_checked: int = 0
    data_files_checked: int = 0
    fixes_applied: List[str] = field(default_factory=list)

    @property
    def has_errors(self) -> bool:
        return any(issue.level == "error" for issue in self.issues)

    @property
    def is_healthy(self) -> bool:
        return len(self.issues) == 0

    def summary(self) -> str:
        lines = [f"Repair report for: {self.table_path}"]
        lines.append(f"  Snapshots checked: {self.snapshots_checked}")
        lines.append(f"  Manifest lists checked: {self.manifest_lists_checked}")
        lines.append(f"  Manifest files checked: {self.manifest_files_checked}")
        lines.append(f"  Data files checked: {self.data_files_checked}")

        if self.is_healthy:
            lines.append("  Status: HEALTHY - no issues found")
        else:
            errors = [i for i in self.issues if i.level == "error"]
            warnings = [i for i in self.issues if i.level == "warning"]
            lines.append(f"  Issues: {len(errors)} error(s), {len(warnings)} warning(s)")
            for issue in self.issues:
                prefix = "ERROR" if issue.level == "error" else "WARN"
                path_str = f" [{issue.path}]" if issue.path else ""
                lines.append(f"    [{prefix}] ({issue.category}) {issue.message}{path_str}")

        if self.fixes_applied:
            lines.append(f"  Fixes applied: {len(self.fixes_applied)}")
            for fix in self.fixes_applied:
                lines.append(f"    - {fix}")

        return "\n".join(lines)


class TableRepair:
    """
    Verifies the metadata chain of a Paimon table.

    The metadata chain is:
      LATEST file -> snapshot -> manifest list files -> manifest files -> data files

    This class checks each link in the chain and reports issues.
    """

    def __init__(self, file_io: FileIO, table_path: str, branch: Optional[str] = None):
        from pypaimon.branch.branch_manager import BranchManager
        from pypaimon.schema.schema_manager import SchemaManager

        self.file_io = file_io
        self.table_path = table_path.rstrip('/')
        self.branch = BranchManager.normalize_branch(branch)

        branch_root = BranchManager.branch_path(self.table_path, self.branch)
        self.snapshot_dir = f"{branch_root}/snapshot"
        self.manifest_dir = f"{self.table_path}/manifest"
        self.latest_file = f"{self.snapshot_dir}/LATEST"

        schema_manager = SchemaManager(file_io, self.table_path, self.branch)
        table_schema = schema_manager.latest()
        if table_schema is not None:
            field_dict = {f.name: f for f in table_schema.fields}
            self.partition_keys_fields = [field_dict[name] for name in table_schema.partition_keys]
            self.default_part_value = table_schema.options.get(
                "partition.default-name", "__DEFAULT_PARTITION__")
        else:
            self.partition_keys_fields = []
            self.default_part_value = "__DEFAULT_PARTITION__"

    def verify(self, check_data_files: bool = False) -> RepairReport:
        """
        Verify the metadata consistency chain.

        Args:
            check_data_files: If True, also verify that data files referenced
                              in manifests actually exist on disk. Time complexity
                              is O(total_data_files) — proportional to the number
                              of data file entries across all manifests.

        Returns:
            RepairReport with all issues found.
        """
        if check_data_files:
            logger.info("Verifying data files for table %s (this may be slow for large tables)",
                        self.table_path)
        report = RepairReport(table_path=self.table_path)

        # Step 1: Check snapshot directory exists
        if not self.file_io.exists(self.snapshot_dir):
            return report

        # Step 2: Find all snapshot files and the LATEST hint
        snapshot_ids = self._list_snapshot_ids()
        latest_id = self._read_latest_id()

        if not snapshot_ids and latest_id is None:
            report.issues.append(RepairIssue(
                level="warning",
                category="snapshot",
                message="No snapshots found - table may be empty or newly created",
            ))
            return report

        # Step 3: Check LATEST consistency
        if latest_id is not None and latest_id not in snapshot_ids:
            report.issues.append(RepairIssue(
                level="error",
                category="snapshot",
                message=f"LATEST file points to snapshot-{latest_id} which does not exist",
                path=self.latest_file,
            ))

        if latest_id is None and snapshot_ids:
            report.issues.append(RepairIssue(
                level="warning",
                category="snapshot",
                message="LATEST file is missing but snapshot files exist",
                path=self.latest_file,
            ))

        # Step 4: Verify each snapshot and its manifest chain
        for sid in sorted(snapshot_ids):
            self._verify_snapshot(sid, report, check_data_files)

        return report

    def _list_snapshot_ids(self) -> List[int]:
        """List all snapshot IDs found in the snapshot directory."""
        try:
            statuses = self.file_io.list_status(self.snapshot_dir)
        except Exception:
            return []

        snapshot_pattern = re.compile(r'^snapshot-(\d+)$')
        ids = []
        for status in statuses:
            name = status.base_name if hasattr(status, 'base_name') else ""
            match = snapshot_pattern.match(name)
            if match:
                ids.append(int(match.group(1)))
        return sorted(ids)

    def _read_latest_id(self) -> Optional[int]:
        """Read the LATEST hint file and return the snapshot ID, or None."""
        try:
            if not self.file_io.exists(self.latest_file):
                return None
            content = self.file_io.read_file_utf8(self.latest_file).strip()
            if not content:
                return None
            return int(content)
        except (ValueError, Exception):
            return None

    def _try_read_snapshot(self, snapshot_id: int) -> Optional[Snapshot]:
        """Try to read and parse a snapshot file. Returns None on failure."""
        snapshot_path = f"{self.snapshot_dir}/snapshot-{snapshot_id}"
        try:
            content = self.file_io.read_file_utf8(snapshot_path)
            return JSON.from_json(content, Snapshot)
        except Exception:
            return None

    def _verify_snapshot(self, snapshot_id: int, report: RepairReport, check_data_files: bool):
        """Verify a single snapshot and its downstream manifest chain."""
        report.snapshots_checked += 1
        snapshot_path = f"{self.snapshot_dir}/snapshot-{snapshot_id}"

        snapshot = self._try_read_snapshot(snapshot_id)
        if snapshot is None:
            report.issues.append(RepairIssue(
                level="error",
                category="snapshot",
                message=f"Snapshot file is unreadable or corrupted (snapshot-{snapshot_id})",
                path=snapshot_path,
            ))
            return

        # Verify base manifest list
        self._verify_manifest_list(
            snapshot.base_manifest_list, f"snapshot-{snapshot_id}/baseManifestList",
            report, check_data_files
        )

        # Verify delta manifest list
        self._verify_manifest_list(
            snapshot.delta_manifest_list, f"snapshot-{snapshot_id}/deltaManifestList",
            report, check_data_files
        )

        # Verify changelog manifest list (optional)
        if snapshot.changelog_manifest_list:
            self._verify_manifest_list(
                snapshot.changelog_manifest_list,
                f"snapshot-{snapshot_id}/changelogManifestList",
                report, check_data_files
            )

    def _verify_manifest_list(self, manifest_list_name: str, context: str,
                              report: RepairReport, check_data_files: bool):
        """Verify a manifest list file exists and is readable."""
        if not manifest_list_name:
            return

        report.manifest_lists_checked += 1
        manifest_list_path = f"{self.manifest_dir}/{manifest_list_name}"

        if not self.file_io.exists(manifest_list_path):
            report.issues.append(RepairIssue(
                level="error",
                category="manifest_list",
                message=f"Manifest list file missing (referenced by {context})",
                path=manifest_list_path,
            ))
            return

        # Try to read it
        try:
            from io import BytesIO
            import fastavro
            with self.file_io.new_input_stream(manifest_list_path) as input_stream:
                avro_bytes = input_stream.read()
            buffer = BytesIO(avro_bytes)
            reader = fastavro.reader(buffer)
            manifest_file_names = []
            for record in reader:
                manifest_file_names.append(record['_FILE_NAME'])
        except Exception as e:
            report.issues.append(RepairIssue(
                level="error",
                category="manifest_list",
                message=f"Manifest list file is corrupted: {e} (referenced by {context})",
                path=manifest_list_path,
            ))
            return

        # Verify each manifest file referenced in this list
        for mf_name in manifest_file_names:
            self._verify_manifest_file(mf_name, manifest_list_name, report, check_data_files)

    def _verify_manifest_file(self, manifest_file_name: str, parent_list: str,
                              report: RepairReport, check_data_files: bool):
        """Verify a manifest file exists and is readable."""
        report.manifest_files_checked += 1
        manifest_path = f"{self.manifest_dir}/{manifest_file_name}"

        if not self.file_io.exists(manifest_path):
            report.issues.append(RepairIssue(
                level="error",
                category="manifest",
                message=f"Manifest file missing (referenced by {parent_list})",
                path=manifest_path,
            ))
            return

        # Always validate the manifest is readable Avro
        try:
            from io import BytesIO
            import fastavro
            with self.file_io.new_input_stream(manifest_path) as input_stream:
                avro_bytes = input_stream.read()
            buffer = BytesIO(avro_bytes)
            reader = fastavro.reader(buffer)
            records = list(reader)
        except Exception as e:
            report.issues.append(RepairIssue(
                level="error",
                category="manifest",
                message=f"Manifest file is corrupted: {e}",
                path=manifest_path,
            ))
            return

        if check_data_files:
            from pypaimon.table.row.generic_row import GenericRowDeserializer
            from pypaimon.utils.file_store_path_factory import _is_null_or_whitespace_only

            for record in records:
                if record.get('_KIND', 0) != 0:
                    continue
                file_dict = record.get('_FILE')
                if not file_dict:
                    continue
                file_name = file_dict.get('_FILE_NAME')
                if not file_name:
                    continue
                report.data_files_checked += 1
                if report.data_files_checked % 1000 == 0:
                    logger.info("Checked %d data files so far...", report.data_files_checked)

                external_path = file_dict.get('_EXTERNAL_PATH')
                if external_path:
                    data_file_path = external_path
                else:
                    # Build path: table_path/<partition_dirs>/bucket-<N>/<file_name>
                    path_builder = self.table_path
                    if self.partition_keys_fields:
                        try:
                            partition = GenericRowDeserializer.from_bytes(
                                record['_PARTITION'], self.partition_keys_fields)
                            partition_dict = partition.to_dict()
                            for field_name, field_value in partition_dict.items():
                                part_value = (self.default_part_value
                                              if _is_null_or_whitespace_only(field_value)
                                              else str(field_value))
                                path_builder = f"{path_builder}/{field_name}={part_value}"
                        except Exception:
                            pass
                    bucket = record.get('_BUCKET', 0)
                    data_file_path = f"{path_builder}/bucket-{bucket}/{file_name}"

                if not self.file_io.exists(data_file_path):
                    report.issues.append(RepairIssue(
                        level="error",
                        category="data_file",
                        message=f"Data file missing (referenced by {manifest_file_name})",
                        path=data_file_path,
                    ))
